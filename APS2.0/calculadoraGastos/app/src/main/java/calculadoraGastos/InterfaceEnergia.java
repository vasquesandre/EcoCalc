    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        String diasUso = fieldDias.getText();
        String horasUso = fieldHoras.getText();
        String wattsAparelho = fieldWatts.getText();
        String custoKwh = fieldCustoKwh.getText();
        
        float valorDias = Float.parseFloat(diasUso);
        float valorHoras = Float.parseFloat(horasUso);
        float valorWatts = Float.parseFloat(wattsAparelho);
        float valorKwh = Float.parseFloat(custoKwh);
        
        Energia energia = new Energia();
        
        float resultado = energia.Calcular(valorDias, valorHoras, valorWatts, valorKwh);
        fieldResultado.setText(String.valueOf(resultado));
    }//GEN-LAST:event_btnCalcActionPerformed

